<p class="BodyText tm69"><strong><span class="tm41">REGLEMENT DES STAGES EN ENTREPRISE</span></strong></p>
<p class="BodyText tm70"><span class="tm71">&#160;</span></p>
<p class="BodyText tm72"><strong><em><span class="tm73"></span></em><span class="tm74">Art.1-</span></strong><span class="tm75"> l&#8217;&eacute;l&egrave;ve ing&eacute;nieur est appel&eacute; &agrave; effectuer un stage de
PFE obligatoire pour l&#8217;obtention du dipl&ocirc;me d&#8217;ing&eacute;nieur en t&eacute;l&eacute;communications. L&#8217;objectif poursuivi du stage de PFE est de donner &agrave; chaque &eacute;tudiant l&#8217;occasion
d&#8217;effectuer une recherche personnelle et approfondie sur un sujet propos&eacute; par une entreprise afin de s&#8217;immerger dans le monde du travail. </span></p>
<p class="BodyText3 tm76"><span class="tm75"></span><strong><span class="tm74">Art.2-</span></strong><span class="tm75"> Pendant la dur&eacute;e de son stage, le stagiaire reste plac&eacute; sous la responsabilit&eacute; de
l&#8217;entreprise d&#8217;accueil tout en demeurant &eacute;tudiant de l&#8217;INPT. L&#39;&eacute;l&egrave;ve stagiaire pourra revenir &agrave; l&#39;Institut pendant la dur&eacute;e du stage, pour y suivre certains cours
demand&eacute;s explicitement par le programme, participer &agrave; des r&eacute;unions; Le cas &eacute;ch&eacute;ant, les dates seront port&eacute;es &agrave; la connaissance de l&#39;Entreprise par l&#39;Etablissement.</span></p>
<p class="BodyText3 tm72"><span class="tm75">Le r&egrave;glement de l&#8217;INPT pr&eacute;voit l&#39;encadrement du stagiaire au cours de sa p&eacute;riode de stage en entreprise. Cet encadrement doit &ecirc;tre assur&eacute;
par un enseignant de l&#8217;INPT et par un membre de l&#39;entreprise charg&eacute; d&#39;accueillir et d&#39;accompagner le stagiaire.</span></p>
<p class="Normal tm77"><span class="tm78"></span><strong><span class="tm75">Art.3-</span></strong><span class="tm78"> Durant son stage, l&#39;&eacute;tudiant stagiaire est soumis &agrave; la discipline et au r&egrave;glement
int&eacute;rieur de l&#39;Entreprise, notamment en ce qui concerne les horaires, </span><strong><span class="tm75">la r&eacute;glementation du travail, les r&egrave;gles d&#8217;hygi&egrave;ne et de s&eacute;curit&eacute;
en vigueur dans l&#8217;entreprise</span></strong><span class="tm78">. </span></p>
<p class="Normal tm79"><span class="tm78">Toute sanction disciplinaire ne peut &ecirc;tre d&eacute;cid&eacute;e que par l&#39;Institut. Dans ce cas, l&#8217;entreprise informe l&#39;Institut des manquements et lui fournit
&eacute;ventuellement les &eacute;l&eacute;ments constitutifs. L&#8217;entreprise, en accord avec le Directeur de l&#39;INPT, peut mettre fin au stage du stagiaire, tout en respectant les dispositions fix&eacute;es &agrave;
l&#8217;article 4 ci-apr&egrave;s.</span></p>
<p class="Normal tm77"><span class="tm78"></span><strong><span class="tm75">Art.4-</span></strong><span class="tm78"> Toute absence devra &ecirc;tre signal&eacute;e par l&#8217;Entreprise &agrave; l&#8217;&eacute;tablissement.</span></p>
<p class="BodyText tm76"><span class="tm75">Dans le cas d&#8217;une interruption, d&#8217;une semaine au moins, pour motif circonstanci&eacute; ou contexte exceptionnel, autoris&eacute;e par l&#8217;entreprise, un avenant
&agrave; la pr&eacute;sente convention devra &ecirc;tre sign&eacute; au pr&eacute;alable par les cocontractants.</span></p>
<p class="BodyText tm76"><span class="tm75">En cas de volont&eacute; d&#8217;une des trois parties (entreprise, INPT, &eacute;tudiant(e)) d&#8217;interrompre d&eacute;finitivement le stage, </span><strong><span
 class="tm74">celle-ci devra imm&eacute;diatement en informer les deux autres parties par &eacute;crit. Les raisons invoqu&eacute;es seront examin&eacute;es en &eacute;troite concertation.</span></strong><span
 class="tm75"> La d&eacute;cision d&eacute;finitive d&#8217;interruption du stage ne sera prise qu&#8217;&agrave; l&#8217;issue de cette phase de concertation. </span></p>
<p class="BodyText tm72"><span class="tm75"></span><strong><span class="tm74">Art.5-</span></strong><span class="tm75"> </span><span
 class="tm75">Le stagiaire est couvert par l&#39;assurance de l&#8217;INPT contre les accidents pouvant survenir au cours du stage dans la limite de garantie de son assurance. </span></p>
<p class="Normal tm79"><span class="tm78"></span><strong><span class="tm75">Art.6-</span></strong><span class="tm78"> A l&#8217;issue du stage, l&#8217;&eacute;tudiant est tenu de pr&eacute;senter les r&eacute;sultats de son
travail, tant par &eacute;crit dans son m&eacute;moire de fin d&#8217;&eacute;tudes, qu&#39;oralement lors de sa soutenance devant un jury comprenant des repr&eacute;sentants de l&#8217;entreprise accueillante et des enseignants
de l&#8217;INPT. La pr&eacute;sentation et le rapport devront avoir &eacute;t&eacute; valid&eacute;s par l&#8217;entreprise au pr&eacute;alable et par l&#8217;enseignant encadrant &agrave; l&#8217;INPT. L&#39;&eacute;tudiant
s&#39;engage &agrave; fournir un rapport &agrave; l&#39;organisme d&#8217;accueil et &agrave; l&#39;INPT au maximum une semaine avant la date de la soutenance. </span></p>
<p class="BodyTextIndent2 tm80"><span class="tm75">A la fin du stage et avant la soutenance, le Directeur de stage mentionn&eacute; dans la convention est pri&eacute; de communiquer &agrave; l&#8217;&eacute;cole une &eacute;valuation
du comportement du stagiaire et de la qualit&eacute; du travail effectu&eacute;. </span></p>
<p class="Normal tm79"><span class="tm78"></span><strong><span class="tm75">Art.7-</span></strong><span class="tm78"> Les &eacute;tudiants stagiaires prennent l&#39;engagement de n&#39;utiliser en aucun cas les informations
recueillies ou obtenues par eux pour en faire l&#39;objet de publication, communication &agrave; des tiers sans accord pr&eacute;alable de la Direction de l&#39;Entreprise, y compris le rapport de stage. Cet engagement vaudra
non seulement pour la dur&eacute;e du stage mais &eacute;galement apr&egrave;s son expiration. L&#39;&eacute;tudiant s&#39;engage &agrave; ne conserver, emporter, ou prendre copie d&#39;aucun document ou logiciel, de quelque
nature que ce soit, appartenant &agrave; l&#39;Entreprise, sauf accord de cette derni&egrave;re. </span><span class="tm78">L&#8217;Entreprise peut demander une restriction de la diffusion du rapport, voire le retrait de certains
&eacute;l&eacute;ments tr&egrave;s confidentiels. Les personnes amen&eacute;es &agrave; en conna&icirc;tre sont contraintes par le secret professionnel &agrave; n&#8217;utiliser ni ne divulguer les informations du rapport.</span></p>
<h3 class="tm81"><strong><span class="tm75"></span><span class="tm75">Art.8- </span></strong><span class="tm78">S&#8217;il advenait qu&#8217;un contrat de travail prenant effet avant la date de fin du stage soit sign&eacute;
avec l&#8217;Entreprise, la pr&eacute;sente convention deviendrait caduque&nbsp;; l&#8217;&eacute;tudiant stagiaire perdrait son statut d&#8217;&eacute;tudiant et ne rel&egrave;verait plus de la responsabilit&eacute; de l&#8217;Ecole.
Ce dernier devrait imp&eacute;rativement en &ecirc;tre averti avant signature du contrat.</span></h3>
