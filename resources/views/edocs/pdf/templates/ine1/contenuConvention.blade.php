        <h3><strong>Pr&eacute;ambule</strong></h3>
        <p><span style="font-weight: 400;">L'INPT forme des ing&eacute;nieurs en R&eacute;seaux de T&eacute;l&eacute;communications, de l&rsquo;Informatique et du Multim&eacute;dia. Ces ing&eacute;nieurs pourront &ecirc;tre de futurs responsables pour des entreprises, dans un monde ouvert aux dynamiques des r&eacute;seaux et des syst&egrave;mes d'informations. </span></p>
        <p><span style="font-weight: 400;">La mise en situation professionnelle est une &eacute;tape importante dans la formation d&rsquo;ing&eacute;nieurs, elle permet aux &eacute;l&egrave;ves de se confronter aux r&eacute;alit&eacute;s techniques, scientifiques, &eacute;conomiques et sociales. C&rsquo;est dans cette optique que l&rsquo;INPT s&rsquo;inscrit et marque sa volont&eacute; d&rsquo;avoir dans ses programmes de formation, des stages de divers types orient&eacute;s vers des objectifs globaux et sp&eacute;cifiques (Stage Ouvrier, Stage Technique et Stage de Projet de Fin d&rsquo;&Eacute;tudes). </span></p>
        <p><span style="font-weight: 400;">Durant le stage ouvrier dont la dur&eacute;e est de 4 semaines ou plus, l&rsquo;&eacute;l&egrave;ve ing&eacute;nieur occupe g&eacute;n&eacute;ralement un poste o&ugrave; il se familiarise avec les conditions de travail et observe le fonctionnement de l&rsquo;organisme d&rsquo;accueil.</span></p>
